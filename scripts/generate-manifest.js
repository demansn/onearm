import path from "path";
import fs from "fs";
import { findGameRoot } from "./utils/find-game-root.js";

const FONT_WEIGHT_MAP = {
    Thin:       { weight: "100" },
    ExtraLight: { weight: "200" },
    UltraLight: { weight: "200" },
    Light:      { weight: "300" },
    Regular:    { weight: "normal" },
    Medium:     { weight: "500" },
    SemiBold:   { weight: "600" },
    DemiBold:   { weight: "600" },
    Bold:       { weight: "bold" },
    ExtraBold:  { weight: "800" },
    UltraBold:  { weight: "800" },
    Black:      { weight: "900" },
    Heavy:      { weight: "900" },
    Italic:     { weight: "normal", style: "italic" },
    BoldItalic: { weight: "bold",   style: "italic" },
};

const FONT_WEIGHTS = Object.keys(FONT_WEIGHT_MAP);

const FONT_EXTENSIONS = new Set([".ttf", ".woff", ".woff2", ".otf"]);
const BITMAP_FONT_EXTENSIONS = new Set([".fnt", ".xml"]);
const IMAGE_EXTENSIONS = new Set([".png", ".jpg", ".jpeg", ".webp", ".svg"]);
const SPINE_SKELETON_EXTENSIONS = new Set([".json", ".skel"]);

/**
 * Generate resources-manifest.js by scanning assets/ directory.
 *
 * Conventions:
 *   fonts/{file}.ttf          → logo bundle, alias = filename sans weight suffix
 *   sound/{file}.mp3          → sounds bundle, alias = filename
 *   spine/{bundle}/{alias}/   → {bundle} bundle, alias = folder name
 *   img/{name}{tps}/            → {name} bundle if it exists (e.g. preloader), else main bundle, spritesheet (packed by AssetPack)
 *   img/{file}.png              → main bundle, loose image with WebP/PNG fallback
 *   spritesheet/{bundle}/*.json → {bundle} bundle, pre-made spritesheet (JSON+PNG)
 */
export function generateManifest(gameRoot) {
    if (!gameRoot) gameRoot = findGameRoot();
    const assetsDir = path.join(gameRoot, "assets");
    const distImgDir = path.join(gameRoot, "dist", "assets", "img");

    const bundles = new Map();
    bundles.set("logo", []);
    bundles.set("preloader", []);
    bundles.set("main", []);
    bundles.set("sounds", []);

    addStaticEntries(assetsDir, bundles);
    discoverFonts(assetsDir, bundles);
    discoverBitmapFonts(assetsDir, bundles);
    discoverSpine(assetsDir, bundles);
    discoverSpritesheets(assetsDir, bundles);
    discoverImages(assetsDir, bundles, distImgDir);
    discoverSounds(assetsDir, bundles);
    discoverPlinko(assetsDir, bundles);

    const manifest = {
        bundles: Array.from(bundles.entries()).map(([name, assets]) => ({ name, assets })),
    };

    const code =
        "// Auto-generated by generate-manifest.js — do not edit manually\n" +
        `export const manifest = ${JSON.stringify(manifest, null, 4)};\n`;

    const outputPath = path.join(gameRoot, "src/configs/resources-manifest.js");

    const existing = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : "";
    if (existing !== code) {
        fs.mkdirSync(path.dirname(outputPath), { recursive: true });
        fs.writeFileSync(outputPath, code);
        console.log("Generated resources-manifest.js");
    } else {
        console.log("resources-manifest.js is up to date");
    }
}

// ---------------------------------------------------------------------------

function ensureBundle(bundles, name) {
    if (!bundles.has(name)) bundles.set(name, []);
    return bundles.get(name);
}

function addStaticEntries(assetsDir, bundles) {
    const statics = [
        { file: "config.json", alias: "config" },
        { file: "components.config.json", alias: "components.config" },
    ];
    for (const { file, alias } of statics) {
        if (fs.existsSync(path.join(assetsDir, file))) {
            ensureBundle(bundles, "logo").push({ alias, src: `./assets/${file}` });
        }
    }

    const scenesDir = path.join(assetsDir, "scenes");
    if (fs.existsSync(scenesDir)) {
        const sceneFiles = fs.readdirSync(scenesDir).filter(f => f.endsWith(".json")).sort();
        for (const file of sceneFiles) {
            const alias = `scene.${path.basename(file, ".json")}`;
            ensureBundle(bundles, "logo").push({ alias, src: `./assets/scenes/${file}` });
        }
    }
}

function discoverFonts(assetsDir, bundles) {
    const fontsDir = path.join(assetsDir, "fonts");
    if (!fs.existsSync(fontsDir)) return;

    const meta = fs.readdirSync(fontsDir)
        .filter((f) => FONT_EXTENSIONS.has(path.extname(f).toLowerCase()))
        .sort()
        .map((file) => {
            const basename = path.basename(file, path.extname(file));
            return { file, basename, ...stripFontWeight(basename) };
        });

    // If multiple files share the same stripped alias (e.g. Inter-Bold + Inter-Regular → "Inter"),
    // fall back to the full basename to keep aliases unique while preserving the shared family.
    const strippedCounts = new Map();
    for (const { stripped } of meta) {
        strippedCounts.set(stripped, (strippedCounts.get(stripped) ?? 0) + 1);
    }

    for (const { file, basename, stripped, suffix } of meta) {
        const alias = strippedCounts.get(stripped) > 1 ? basename : stripped;
        const family = camelCaseToSpaces(stripped);
        const cssDesc = (suffix && FONT_WEIGHT_MAP[suffix]) || { weight: "normal" };

        const data = { family, weights: [cssDesc.weight] };
        if (cssDesc.style) data.style = cssDesc.style;

        ensureBundle(bundles, "logo").push({
            alias,
            src: `./assets/fonts/${file}`,
            data,
        });
    }
}

function discoverBitmapFonts(assetsDir, bundles) {
    const bitmapFontsDir = path.join(assetsDir, "bitmap-fonts");
    if (!fs.existsSync(bitmapFontsDir)) return;

    const files = fs.readdirSync(bitmapFontsDir)
        .filter((f) => BITMAP_FONT_EXTENSIONS.has(path.extname(f).toLowerCase()))
        .sort();

    for (const file of files) {
        const alias = path.basename(file, path.extname(file));
        ensureBundle(bundles, "logo").push({
            alias,
            src: `./assets/bitmap-fonts/${file}`,
        });
    }
}

function discoverSounds(assetsDir, bundles) {
    const soundDir = path.join(assetsDir, "sound");
    if (!fs.existsSync(soundDir)) return;

    const allFiles = new Set(fs.readdirSync(soundDir));
    const mp3Files = [...allFiles].filter((f) => f.endsWith(".mp3")).sort();

    for (const file of mp3Files) {
        const alias = path.basename(file, ".mp3");
        const oggFile = `${alias}.ogg`;

        const src = allFiles.has(oggFile)
            ? [`./assets/sound/${oggFile}`, `./assets/sound/${file}`]
            : `./assets/sound/${file}`;

        ensureBundle(bundles, "sounds").push({ alias, src });
    }
}

function discoverSpine(assetsDir, bundles) {
    const spineDir = path.join(assetsDir, "spine");
    if (!fs.existsSync(spineDir)) return;

    // spine/{bundle}/{dir}/
    const bundleDirs = readDirs(spineDir);

    for (const bundleName of bundleDirs) {
        const bundlePath = path.join(spineDir, bundleName);

        // Handle loose files directly in bundle dir (e.g. spine/main/transition.json)
        const bundleFiles = fs.readdirSync(bundlePath, { withFileTypes: true });
        const looseFiles = bundleFiles.filter((f) => f.isFile()).map((f) => f.name);
        addSpineEntries(bundles, bundleName, `./assets/spine/${bundleName}`, looseFiles);

        // Handle subdirectories
        const aliasDirs = readDirs(bundlePath);
        for (const dir of aliasDirs) {
            const dirPath = path.join(bundlePath, dir);
            const files = fs.readdirSync(dirPath);
            addSpineEntries(bundles, bundleName, `./assets/spine/${bundleName}/${dir}`, files);
        }
    }
}

function addSpineEntries(bundles, bundleName, base, files) {
    // Each skeleton file → separate alias by filename
    const skeletonFiles = files.filter((f) =>
        SPINE_SKELETON_EXTENSIONS.has(path.extname(f).toLowerCase()),
    );

    for (const skeletonFile of skeletonFiles) {
        const alias = path.basename(skeletonFile, path.extname(skeletonFile));
        ensureBundle(bundles, bundleName).push({
            alias: `${alias}Data`,
            src: `${base}/${skeletonFile}`,
        });
    }

    // Atlas alias by atlas filename
    const atlasFiles = files.filter((f) => f.endsWith(".atlas"));
    for (const atlasFile of atlasFiles) {
        const alias = path.basename(atlasFile, ".atlas");
        ensureBundle(bundles, bundleName).push({
            alias: `${alias}Atlas`,
            src: `${base}/${atlasFile}`,
        });
    }
}

function discoverImages(assetsDir, bundles, distImgDir) {
    const imgDir = path.join(assetsDir, "img");
    if (!fs.existsSync(imgDir)) return;

    const entries = fs.readdirSync(imgDir, { withFileTypes: true });

    // {tps} folders → read actual filenames from AssetPack manifest
    const tpsDirs = entries.filter((e) => e.isDirectory() && e.name.includes("{tps}"));

    if (tpsDirs.length > 0) {
        const assetPackManifestPath = path.join(distImgDir, "manifest.json");
        if (!fs.existsSync(assetPackManifestPath)) {
            console.warn("Warning: AssetPack manifest not found at", assetPackManifestPath);
            console.warn("Run asset packing before generating the manifest.");
        } else {
            const apManifest = JSON.parse(fs.readFileSync(assetPackManifestPath, "utf8"));
            const apAssets = apManifest.bundles.flatMap((b) => b.assets);

            for (const entry of tpsDirs.sort((a, b) => a.name.localeCompare(b.name))) {
                const name = entry.name.replace("{tps}", "");
                const apEntry = apAssets.find((a) => a.alias.includes(name));
                if (apEntry) {
                    const bundleName = bundles.has(name) ? name : "main";
                    ensureBundle(bundles, bundleName).push({
                        alias: `${name}-sprites`,
                        src: apEntry.src.map((s) => `./assets/img/${s}`),
                    });
                } else {
                    console.warn(`Warning: No AssetPack entry found for "${name}" spritesheet`);
                }
            }
        }
    }

    // Loose image files → WebP + PNG fallback
    for (const entry of entries.filter((e) => e.isFile()).sort((a, b) => a.name.localeCompare(b.name))) {
        const ext = path.extname(entry.name).toLowerCase();
        if (!IMAGE_EXTENSIONS.has(ext)) continue;
        const basename = path.basename(entry.name, ext);
        ensureBundle(bundles, "main").push({
            alias: basename,
            src: [`./assets/img/${basename}.webp`, `./assets/img/${basename}.png`],
        });
    }
}

function discoverSpritesheets(assetsDir, bundles) {
    const sheetDir = path.join(assetsDir, "spritesheet");
    if (!fs.existsSync(sheetDir)) return;

    // spritesheet/{bundle}/{name}.json + .png
    for (const bundleName of readDirs(sheetDir)) {
        const bundlePath = path.join(sheetDir, bundleName);
        const jsonFiles = fs.readdirSync(bundlePath)
            .filter((f) => f.endsWith(".json"))
            .sort();

        for (const file of jsonFiles) {
            const alias = path.basename(file, ".json");
            ensureBundle(bundles, bundleName).push({
                alias,
                src: `./assets/spritesheet/${bundleName}/${file}`,
            });
        }
    }
}

function discoverPlinko(assetsDir, bundles) {
    const plinkoDir = path.join(assetsDir, "plinko");
    if (!fs.existsSync(plinkoDir)) return;

    for (const theme of readDirs(plinkoDir)) {
        const themeDir = path.join(plinkoDir, theme);
        const jsonFiles = fs.readdirSync(themeDir)
            .filter((f) => f.endsWith(".json"))
            .sort();

        for (const file of jsonFiles) {
            const alias = path.basename(file, ".json");
            ensureBundle(bundles, `plinko-${theme}`).push({
                alias,
                src: `./assets/plinko/${theme}/${file}`,
            });
        }
    }
}

// ---------------------------------------------------------------------------
// Helpers

function readDirs(dir) {
    return fs.readdirSync(dir, { withFileTypes: true })
        .filter((d) => d.isDirectory())
        .map((d) => d.name)
        .sort();
}

function stripFontWeight(name) {
    for (const weight of FONT_WEIGHTS) {
        if (name.endsWith(`-${weight}`)) {
            return { stripped: name.slice(0, -(weight.length + 1)), suffix: weight };
        }
    }
    return { stripped: name, suffix: null };
}

function camelCaseToSpaces(str) {
    return str.replace(/([a-z])([A-Z])/g, "$1 $2");
}

// ---------------------------------------------------------------------------
// CLI

const isMainModule =
    process.argv[1] &&
    path.resolve(process.argv[1]) === path.resolve(new URL(import.meta.url).pathname);

if (isMainModule) {
    generateManifest();
}
